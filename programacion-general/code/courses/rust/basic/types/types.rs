fn main() {
    // Integers types
    let x: i32 = -42;
    let y: u64 = 100;
    println!("Signed integer: {}", x);
    println!("Unsigned integer: {}", y);

    // Floating-point types
    let pi: f64 = 3.14159;
    println!("Value of pi: {}", pi);

    // Boolean type
    let is_snowing: bool = true;
    println!("Is is snowing? {}", is_snowing);

    // Character type
    let letter: char = 'a';
    println!("first letter of the alphabet: {}", letter);
}
